IMenuHotkeysHandler::IMenuHotkeysHandler()
{
}

//-----------------------------------------------------------------------------
IMenuHotkeysHandler::~IMenuHotkeysHandler()
{
}

//-----------------------------------------------------------------------------
std::string IMenuHotkeysHandler::formatHotkeyInfoString(std::string key,
                                                           std::string description,
                                                           int line_width) {
    std::stringstream ss;
    int left_col_width = (line_width/2) - 2;
    int right_col_width = line_width/2;

    ss << std::setfill('.')
              << std::left
              << std::setw(left_col_width)
              << ("<" + key + ">")
              << std::right
              << std::setw(right_col_width)
              << description
              << std::endl;

    return ss.str();
}

